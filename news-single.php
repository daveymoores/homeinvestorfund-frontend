				<p>“Never put all your eggs in one basket” is sound advice in the investment field as it is in any other. Diversification of an investment portfolio, of course, means holding a variety of different asset classes, and across different sub-sectors and geographic locations.</p>

				<p>Diversification is important as different types of assets behave in different ways depending on the economic factors driving them. Sentiment of investors can also play a significant part in short-term performance of different assets. As values of different asset classes tend not to head in the same direction at the same time, diversification can enhance the volatility/return profile of an investment portfolio. The chart below shows how different fund sectors have performed by looking back at the annual return every month for the past ten years.</p>

				<p>As you would expect, of the ‘traditional’ asset classes, equities tend to be the most volatile but offer the potential for high returns. Cash is the most stable asset class but offers little prospect for investment return, often providing negative returns in ‘real’ terms after allowing for the effects of inflation. Others such as fixed income and commercial property tend to sit somewhere between them.</p>

